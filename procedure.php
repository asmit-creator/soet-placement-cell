<div class="procde">
	<h2>Procedure for Companies</h2>
	<p>
	<ol>
	<li>
	The Placement Cell, School of Engineering and Technology (a student body) sends an invitation to various companies along with relevant details.
	</li>
	<li>
	Interested companies fills <a href="jaf.php">Job Announcement Form (JAF)</a>. 
	<li>Login credentials are mailed within 24 hours of posting JAF to access online account. JAF contains detail of the
	job offer (pay package, place of posting, allowances and other bonuses).
	</li>
	<li>
	The company can set criteria for their different Job Offers through their online account. 
	</li>
	<li>
	If the company/ organization wishes to conduct a Pre-Placement Talk (PPT) they can mail a request along with the preferred dates or can put it in logical requirement in JAF.
	</li>
	<li>
	Once drive is scheduled, drive date will be updated at company's online platform.
	</li>
	<li>
	The company will be informated about interested students by placement cell via email, call.
	</li>
	<li>
	Companies come down to the campus on the allotted date/s and conduct tests and/or interviews according to their recruitment process.
	<li>
	The company/ organization is required to furnish the final list of students preferably on the date of interview.
	Note: The placement office records jobs corresponding to the students selected. Students once placed may not be allowed 
	to appear for other interviews as per the institute placement policy.<br>
	</ol>
	* The Job Announcement Form provides the primary basis of communicating the details of the positions offered to 
	the candidates. It is therefore highly desirable that the Form is completed in all respects and it would be advantageous if 
	it were accompanied by relevant company literature with more details about the company.<br>

	**Companies are allotted dates based on the ranking of the job offer. The job offer is ranked based on the following parameters:
	<ul>
	<li>
	Compensation Package
	</li>
	<li>
	Growth Prospects in the job
	</li>
	<li>
	A Past record of recruitment
	</li>
	<li>
	The data provided by the companies through the Job Announcement Form, company brochures including 
	financial statements, etc.
	</li></ul>
	Hoping for your kind co-operation for a bright and blooming placement season.<br>
	<b>Student Placement Representatives,<br>
	Placement Cell,<br>
	School of Engineering and Technology, HNBGU.<b>

